
var x = 5;
switch (x) { 
	case 1:
		console.log("Number 1");
		break; 
	case 5: 
		console.log("Number 5");
	    break; 
	default:
		console.log("Error");
		break; 
}
var y = "red";
switch (y) { 
	case "red":
		console.log("Color red");
		break; 
	case "blue": 
		console.log("Color blue");
	    break; 
	default:
		console.log("Error");
		break; 
}